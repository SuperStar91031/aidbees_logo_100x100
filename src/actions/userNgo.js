import {
  DATA_SUCCESS,
  DATA_FAIL,
  SET_MESSAGE,
} from "./types";

import UserNgoService from "../services/userNgo.service";

export const ngoListFilter = (logged_user_id, ngoListFilterName, ngoListFilterCatID, ngoListFilterSubCatID,ngoListFilterLastID,ngoListFilterPageRecords,ngoListFilterLastName) => (dispatch) => {
	console.log("changes "+logged_user_id)
  return UserNgoService.ngoListFilter(logged_user_id, ngoListFilterName, ngoListFilterCatID, ngoListFilterSubCatID,ngoListFilterLastID,ngoListFilterPageRecords,ngoListFilterLastName).then(
    (response) => {
      dispatch({
        type: DATA_SUCCESS,
      });

      dispatch({
        type: SET_MESSAGE,
        payload: response.data.message,
      });

      return Promise.resolve(response);
    },
    (error) => {
      const message =
        (error.response &&
          error.response.data &&
          error.response.data.message) ||
        error.message ||
        error.toString();

      dispatch({
        type: DATA_FAIL,
      });

      dispatch({
        type: SET_MESSAGE,
        payload: message,
      });

      return Promise.reject();
    }
  );
};

